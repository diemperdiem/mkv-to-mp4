# Definir la ruta a la carpeta que contiene los archivos .mkv
$sourceFolder = ".\"

# Definir la carpeta de destino donde quieres guardar los archivos convertidos a .mp4
# Puede ser la misma que la carpeta de origen si lo deseas
$destinationFolder = ".\"

# Obtener todos los archivos .mkv en la carpeta de origen
$mkvFiles = Get-ChildItem -Path $sourceFolder -Filter *.mkv

foreach ($file in $mkvFiles) {
    # Construir el nombre del archivo de salida reemplazando la extensión del archivo
    $outputFileName = [System.IO.Path]::ChangeExtension($file.Name, ".mp4")
    $outputFilePath = Join-Path -Path $destinationFolder -ChildPath $outputFileName

    # Usar FFmpeg para convertir el archivo
    ffmpeg -i "$($file.FullName)" -codec copy "$outputFilePath"
}

# Una vez terminada la conversión, borrar los archivos .m4a
Remove-Item *.mkv -Recurse

